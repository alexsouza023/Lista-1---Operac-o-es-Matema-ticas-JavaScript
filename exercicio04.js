/*  Construa um algoritmo que leia um número inteiro na tela e
responda o número antecessor e o sucessor. */


function antecessorESucessor(numero) {
    const antecessor = numero - 1;
    const sucessor = numero + 1;
    console.log(`Antecessor: ${antecessor}, Sucessor: ${sucessor}`);
}


antecessorESucessor(11);

